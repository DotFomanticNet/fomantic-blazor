
namespace Fomantic.Blazor.UI
{
    /// <summary>   Fomantic UI content horizontal alignment. </summary>
    public enum ContentHorizontalAlignment
    {  /// <summary>
       /// Align content to left
       /// </summary>
        Left=0,
        /// <summary>
        /// Align content to right
        /// </summary>  
        Right,  
        /// <summary>
        /// Align content in the center
        /// </summary>
        Center,
        /// <summary>
        /// Align content to be justified
        /// </summary>
        justified
    }

    /// <summary>   Fomantic UI content vertical alignment. </summary>
    public enum ContentVerticalAlignment
    {  /// <summary>
       /// Align content in the center
       /// </summary>
        Center = 0,
        /// <summary>
        /// Align content to Top
        /// </summary>
        Top,
        /// <summary>
        /// Align content in the Bottom
        /// </summary>
        Bottom,

    }
}
